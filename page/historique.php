          <h3 style="padding-top: 10px">historique de l'iai-cameroun</h3>
            <p>
              <b>L’Institut Africain d’Informatique (I.A.I)</b>, est une Ecole inter-Etats créée par Décision des Chefs d’Etat en 1971 à Fort-Lamy (l’actuel N’Djamena) au Tchad et dotée d’un Statut diplomatique, avec pour Siège Libreville au Gabon. Elle a pour vocation tout d’abord, d’assurer les formations supérieures d’excellence dont l’économie nationale et régionale ont besoin dans un contexte de mondialisation de la connaissance et dans un  rythme accéléré de développement technologique, mais également de promouvoir et de vulgariser l’outil informatique auprès de ses onze Etats membres.
            </p>
            <p>
              <b>L’iai-Cameroun, Représentation Camerounaise de l’I.A.I</b>, existe depuis le 1er octobre 1999 : création réalisée par l’Etat Camerounais sous la haute impulsion du <b>Président de la République, Son Excellence Paul BIYA</b> qui a instruit aussitôt la signature d’un accord de siège entre l’Institut Africain d’Informatique et la République camerounaise.
              Depuis sa création, l’IAI-Cameroun a connu un développement progressif et harmonieux dont les résultats sont visibles sur tous les plans : académique, managérial, logistique et infrastructurel. Son Siège est aujourd’hui situé au quartier Dragages à Yaoundé.

